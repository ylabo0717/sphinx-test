class MyModule2:
    """
    This class provides basic mathematical operations.
    """

    def add(a, b):
        """
        Adds two numbers.

        Parameters:
        a (int): The first number.
        b (int): The second number.

        Returns:
        int: The sum of the two numbers.
        """
        return a + b

    def subtract(a, b):
        """
        Subtracts two numbers.

        Parameters:
        a (int): The first number.
        b (int): The second number.

        Returns:
        int: The difference between the two numbers.
        """
        return a - b

    def multiply(a, b):
        """
        Multiplies two numbers.

        Parameters:
        a (int): The first number.
        b (int): The second number.

        Returns:
        int: The product of the two numbers.
        """
        return a * b

    def divide(a, b):
        """
        Divides two numbers.

        Parameters:
        a (int): The dividend.
        b (int): The divisor.

        Returns:
        float: The quotient of the division.
        """
        return a / b
